/*ADDING TWO POINT VALUES AND PRINT THE RESULT*/\
#include<stdio.h>
void main()
{
	//variables declaration
	float number1,number2,sum;
	
	//initilization
	number1 = 10.5;
	number2 = 9.5;
	
	//caculation
	sum = number1+number2; // 10.5+9.5 = 20
	
	// output
	printf("sum of %f and %f: %f",number1,number2,sum);
}
